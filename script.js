document.getElementById('contactForm').addEventListener('submit', function(event) {
    event.preventDefault(); 

    let isValid = true;

    // Function to validate input fields
    function validateField(field, errorField) {
        if (field.value.trim() === '') {
            field.classList.add('input-error');
            errorField.style.display = 'block';
            isValid = false;
        } else {
            field.classList.remove('input-error');
            errorField.style.display = 'none';
        }
    }

    // Function to validate checkboxes
    function validateCheckboxes() {
        const checkboxes = document.querySelectorAll('.form-check-input');
        const errorField = document.getElementById('serviceTypeError');
        const isChecked = Array.from(checkboxes).some(checkbox => checkbox.checked);

        if (!isChecked) {
            errorField.style.display = 'block';
            isValid = false;
        } else {
            errorField.style.display = 'none';
        }
    }

    // Validate each field
    validateField(document.getElementById('name'), document.getElementById('nameError'));
    validateField(document.getElementById('email'), document.getElementById('emailError'));
    validateField(document.getElementById('phone-number'), document.getElementById('phoneError'));
    validateField(document.getElementById('service-description'), document.getElementById('descriptionError'));

    // Validate checkboxes
    validateCheckboxes();





    
});



